
public class TiposPrimitivos {
	
public static void main(String[] args) {
		
		byte variavelByte = 1; // -128 até 127
		
		short variavelShort = 1; // -32768 até 32767
		
		int variavelInteger = 1; // -2147483648 até 2147483647
		
		long variavelLong = 1L; // -9,223372037x10¹8 até 9,223372037x10¹8
		
		float variavelFloat = 1.0f;
		
		double variavelDouble = 1.0;
		
		char variavelCharacter = 'A';
		
		boolean variavelBoolean = true;
		
		
	}


}
